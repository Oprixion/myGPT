# Training the BLM

# Create Torch optimizer
optimizer = torch.optim.AdamW(m.parameters(), lr=1e-3)

# Training loop
batch_size = 32 # 32 Parallel sequences
epochs = 10000

for epoch in range(epochs):
  xb, yb = get_batch('train')

  # Evaluate loss
  logits, loss = m(xb, yb)
  optimizer.zero_grad(set_to_none=True)
  loss.backward()
  optimizer.step()

print(loss.item())

